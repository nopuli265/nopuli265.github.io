public class Simple33 {
		public static void main (String args[]) {
		int x;
		for(x=0; x<10; x++){
			if(x==1)
				System.out.println("x=1");
			else if(x==2)
				System.out.println("x=2");
			else if(x==3)
				System.out.println("x=3");
			else if(x==4)
				System.out.println("x=4");
			else
				System.out.println("x не попадает в интервал от 1 до 4 ");
		}

					

	}

}
